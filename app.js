const dealList = document.querySelector("#dealList");
const mapView = document.querySelector("#mapView");
const mapCanvas = document.querySelector("#mapCanvas");
const template = document.querySelector("#dealCardTemplate");
const liveCount = document.querySelector("#liveCount");
const chatWindow = document.querySelector("#chatWindow");
const chatForm = document.querySelector("#chatForm");
const chatInput = document.querySelector("#chatInput");
const dealPreview = document.querySelector("#dealPreview");

function on(selector, eventName, handler) {
  const node = document.querySelector(selector);
  if (node) node.addEventListener(eventName, handler);
}

function t(key) {
  return translations[state.language][key] || translations.en[key] || key;
}

function applyTranslations() {
  document.documentElement.lang = state.language;
  document.querySelector("#languageToggle").textContent = state.language === "en" ? "IT" : "EN";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });
}

function filteredDeals() {
  return deals
    .filter((deal) => state.category === "all" || deal.category === state.category)
    .sort((a, b) => {
      if (state.sort === "ending") return a.expiresIn - b.expiresIn;
      if (state.sort === "discount") return parseDiscount(b.discount) - parseDiscount(a.discount);
      if (state.sort === "newest") return a.created - b.created;
      return a.distance - b.distance;
    });
}

function parseDiscount(value) {
  const match = value.match(/\d+/);
  return match ? Number(match[0]) : 0;
}

function dealTitle(deal) {
  return state.language === "it" && deal.titleIt ? deal.titleIt : deal.title;
}

function localizeDraftValue(label, value) {
  if (state.language !== "it") return value;

  const replacements = {
    "30% off pizza": "30% sulla pizza",
    "10 lunch menus left": "10 menu pranzo rimasti",
    "Extended aperitivo deal": "Offerta aperitivo estesa",
    "Last-minute local offer": "Offerta locale last-minute",
    "Food": "Cibo",
    "Drinks": "Drink",
    "Today, 6pm to 8pm": "Oggi, dalle 18 alle 20",
    "Today until 9pm": "Oggi fino alle 21",
    "Today until 10pm": "Oggi fino alle 22",
    "Today, needs end time": "Oggi, orario finale da confermare",
    "No limit": "Nessun limite",
    "Show code FLASH30": "Mostra codice FLASH30",
    "Dine-in only": "Solo sul posto",
    "Merchant confirms before publishing": "L'esercente conferma prima della pubblicazione",
    "Discount pending": "Sconto da confermare"
  };

  if (replacements[value]) return replacements[value];
  if (label === "Quantity" && value.endsWith(" available")) return `${value.replace(" available", "")} disponibili`;
  return value;
}

function renderDeals() {
  if (!dealList || !template) return;
  const visible = filteredDeals();
  if (liveCount) liveCount.textContent = deals.length;
  dealList.innerHTML = "";

  if (!visible.length) {
    const empty = document.createElement("p");
    empty.className = "empty-state";
    empty.textContent = t("noDeals");
    dealList.append(empty);
    return;
  }

  visible.forEach((deal) => {
    const card = template.content.firstElementChild.cloneNode(true);
    card.querySelector("img").src = deal.image;
    card.querySelector("img").alt = deal.business;
    card.querySelector(".category").textContent = categoryLabels[deal.category][state.language];
    card.querySelector(".distance").textContent = `${deal.distance.toFixed(1)} km`;
    card.querySelector("h3").textContent = dealTitle(deal);
    card.querySelector(".business").textContent = deal.business;
    card.querySelector(".description").textContent = state.language === "it" && deal.descriptionIt ? deal.descriptionIt : deal.description;
    card.querySelector(".discount").textContent = deal.discount;
    card.querySelector(".expires").textContent = `${deal.expiresIn} min`;
    card.querySelector(".redeem").textContent = t("redeem");
    card.querySelector(".share").textContent = t("share");
    card.querySelector(".directions").textContent = t("directions");
    card.querySelector(".redeem").addEventListener("click", () => showRedemption(deal));
    card.querySelector(".share").addEventListener("click", () => shareDeal(deal));
    card.querySelector(".directions").addEventListener("click", () => openDirections(deal));
    dealList.append(card);
  });
}

function renderMap() {
  if (!mapCanvas) return;
  mapCanvas.innerHTML = "";
  filteredDeals().forEach((deal) => {
    const pin = document.createElement("button");
    pin.className = "pin";
    pin.style.left = `calc(${deal.x}% - 58px)`;
    pin.style.top = `calc(${deal.y}% - 28px)`;
    pin.innerHTML = `<strong>${deal.business}</strong><span>${deal.discount} · ${deal.distance.toFixed(1)} km</span>`;
    pin.addEventListener("click", () => showRedemption(deal));
    mapCanvas.append(pin);
  });
}

function showRedemption(deal) {
  const code = deal.business.split(" ")[0].toUpperCase().replace(/[^A-Z]/g, "").slice(0, 5) || "FLASH";
  const codeLabel = state.language === "it" ? "Codice" : "Code";
  alert(`${dealTitle(deal)}\n${deal.address}\n${codeLabel}: ${code}${deal.id}`);
}

function shareDeal(deal) {
  const text = `${deal.business}: ${dealTitle(deal)} (${deal.discount})`;
  const url = `https://wa.me/?text=${encodeURIComponent(text)}`;
  window.location.href = url;
}

function openDirections(deal) {
  const url = `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(`${deal.business} ${deal.address} Milan`)}`;
  window.location.href = url;
}

function renderPreview() {
  if (!dealPreview) return;
  const labels = previewLabels[state.language];
  const rows = [
    [labels[0], localizeDraftValue("Title", state.draft.title)],
    [labels[1], localizeDraftValue("Discount", state.draft.price)],
    [labels[2], localizeDraftValue("Category", state.draft.category)],
    [labels[3], localizeDraftValue("Valid", state.draft.valid)],
    [labels[4], localizeDraftValue("Quantity", state.draft.quantity)],
    [labels[5], localizeDraftValue("Redemption", state.draft.redemption)],
    [labels[6], state.draft.location],
    [labels[7], localizeDraftValue("Terms", state.draft.terms)]
  ];
  dealPreview.innerHTML = rows.map(([key, value]) => `<dt>${key}</dt><dd>${value}</dd>`).join("");
}

function resetChatIntro() {
  if (!chatWindow) return;
  chatWindow.innerHTML = "";
  addBubble(t("aiIntro"));
}

function addBubble(text, type = "ai") {
  if (!chatWindow) return;
  const bubble = document.createElement("div");
  bubble.className = `bubble ${type}`;
  bubble.textContent = text;
  chatWindow.append(bubble);
  chatWindow.scrollTop = chatWindow.scrollHeight;
}

function parseMerchantMessage(message) {
  const lower = message.toLowerCase();

  if (lower.includes("pause")) {
    addBubble(t("paused"));
    return;
  }

  if (lower.includes("extend")) {
    state.draft.valid = "Today until 9pm";
    state.draft.title = "Extended aperitivo deal";
    state.draft.category = "Drinks";
    renderPreview();
    addBubble(t("extended"));
    return;
  }

  const percent = message.match(/(\d+)\s*%/);
  const euro = message.match(/(?:€|euro|eur)\s?(\d+)|(\d+)\s?(?:€|euro|eur)/i);
  const quantity = message.match(/(\d+)\s+(?:lunch|menus|spots|items|left)/i);
  const timeRange = message.match(/from\s+([0-9apm:\s]+)\s+to\s+([0-9apm:\s]+)/i);
  const until = message.match(/until\s+([0-9apm:\s]+)/i);

  const isPizza = lower.includes("pizza");
  const isLunch = lower.includes("lunch") || lower.includes("menu");
  const isAperitivo = lower.includes("aperitivo");

  state.draft = {
    title: isPizza ? "30% off pizza" : isLunch ? "10 lunch menus left" : isAperitivo ? "Aperitivo deal" : "Last-minute local offer",
    price: percent ? `${percent[1]}% off` : euro ? `€${euro[1] || euro[2]}` : "Discount pending",
    category: isAperitivo ? "Drinks" : "Food",
    valid: timeRange ? `Today, ${timeRange[1].trim()} to ${timeRange[2].trim()}` : until ? `Today until ${until[1].trim()}` : "Today, needs end time",
    quantity: quantity ? `${quantity[1]} available` : "No limit",
    redemption: "Show code FLASH30",
    location: "Forno Rosso, Via Lecco 7",
    terms: "Merchant confirms before publishing"
  };

  renderPreview();
  addBubble(t("confirm"));
}

function publishDraft() {
  const newDeal = {
    id: deals.length + 1,
    business: "Forno Rosso",
    title: state.draft.title,
    titleIt: state.draft.title,
    description: `${state.draft.price}. ${state.draft.redemption}.`,
    descriptionIt: `${state.draft.price}. ${state.draft.redemption}.`,
    category: state.draft.category.toLowerCase() === "drinks" ? "drinks" : "food",
    discount: state.draft.price,
    distance: 0.2,
    expiresIn: 87,
    created: 0,
    views: 0,
    going: 0,
    address: "Via Lecco 7",
    image: "https://images.unsplash.com/photo-1565299624946-b28f40a0ae38?auto=format&fit=crop&w=600&q=80",
    x: 57,
    y: 39
  };
  deals.unshift(newDeal);
  renderDeals();
  renderMap();
  addBubble(t("published"));
}

on("#languageToggle", "click", () => {
  state.language = state.language === "en" ? "it" : "en";
  applyTranslations();
  renderPreview();
  renderDeals();
  resetChatIntro();
});

on("#browseDeals", "click", () => {
  document.querySelector("#dealList")?.scrollIntoView({ behavior: "smooth" });
});

on("#useLocation", "click", () => {
  const locationText = document.querySelector("#locationText");
  if (locationText) locationText.textContent = t("locationGranted");
});

document.querySelectorAll(".chip").forEach((chip) => {
  chip.addEventListener("click", () => {
    document.querySelectorAll(".chip").forEach((item) => item.classList.remove("active"));
    chip.classList.add("active");
    state.category = chip.dataset.category;
    renderDeals();
    renderMap();
  });
});

document.querySelectorAll("[data-view]").forEach((button) => {
  button.addEventListener("click", () => {
    document.querySelectorAll("[data-view]").forEach((item) => item.classList.remove("active"));
    button.classList.add("active");
    state.view = button.dataset.view;
    dealList.classList.toggle("hidden", state.view !== "list");
    mapView.classList.toggle("hidden", state.view !== "map");
    renderMap();
  });
});

on("#sortSelect", "change", (event) => {
  state.sort = event.target.value;
  renderDeals();
  renderMap();
});

if (chatForm && chatInput) {
  chatForm.addEventListener("submit", (event) => {
    event.preventDefault();
    const message = chatInput.value.trim();
    if (!message) return;
    addBubble(message, "merchant");
    chatInput.value = "";
    parseMerchantMessage(message);
  });
}

document.querySelectorAll(".quick-actions button").forEach((button) => {
  button.addEventListener("click", () => {
    if (!chatInput || !chatForm) return;
    chatInput.value = button.dataset.message;
    chatForm.requestSubmit();
  });
});

on("#publishDeal", "click", publishDraft);

applyTranslations();
renderPreview();
renderDeals();
renderMap();
resetChatIntro();
